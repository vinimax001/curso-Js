//Contando até 10 crescente e decrescente com variaveis de repetição (while)

//Enquanto (while)
//colocando o cont antes do log utilizar <=

//decrescente

var conta = 10
while (conta >= 1){
    console.log(`Passo ${conta}`)
    conta -- // conta recebe cont =+ 1
}


var con = 10
while (con >= 1){     
    console.log(`volta ${con}`)
    con-- //- 1 a cada repetição
}



//Crescente
var c = 1
while (c <= 10){    
    console.log(`log ${c}`)
    c ++ // cont recebe cont =+ 1 
}

var cont = 1
while (cont <= 10){    
    console.log(`${cont}° Vez`)
    cont++
}